# ---------------------------------------------------------------------------
# Шаг 3: LoRA-адаптер и обучение
# ---------------------------------------------------------------------------

print("=" * 60)
print("  ШАГ 3: Файнтюнинг (LoRA + SFT)")
print("=" * 60)

lora_config = LoraConfig(
    task_type=TaskType.CAUSAL_LM,
    r=8,
    lora_alpha=16,
    lora_dropout=0.05,
    target_modules=["c_attn"],
)
model = get_peft_model(base_model, lora_config)
model.print_trainable_parameters()

dataset = Dataset.from_list(TRAINING_DATA)
print(f"Датасет: {len(dataset)} примеров из T&D-домена\n")

training_args = TrainingArguments(
    output_dir=OUTPUT_DIR,
    num_train_epochs=3,
    per_device_train_batch_size=2,
    learning_rate=2e-4,
    warmup_steps=5,
    logging_steps=5,
    save_strategy="no",
    report_to="none",
    no_cuda=True,
)

trainer = SFTTrainer(
    model=model,
    train_dataset=dataset,
    args=training_args,
    dataset_text_field="text",
    max_seq_length=256,
)

trainer.train()

model.save_pretrained(OUTPUT_DIR)
tokenizer.save_pretrained(OUTPUT_DIR)
print(f"\nАдаптер сохранён → {OUTPUT_DIR}\n")

